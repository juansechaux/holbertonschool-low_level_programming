#include<stdio.h>
#include"3-calc.h"

/**
 * op_add - funtion
 *@a: int
 *@b: int
 *Return: Result
 */

int op_add(int a, int b)
{

	return (a + b);
}

/**
 * op_sub - funtion
 *@a: int
 *@b: int
 *Return: Result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - funtion
 *@a: int
 *@b: int
 *Return: Result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - funtion
 *@a: int
 *@b: int
 *Return: Result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - funtion
 *@a: int
 *@b: int
 *Return: Result
 */

int op_mod(int a, int b)
{
	return (a % b);
}

